import math

def euler_phi(n):
    """
    Функция Эйлера φ(n) — считает количество чисел от 1 до n, которые взаимно просты с n.

    Взаимно простые числа — это такие, у которых с n нет общих делителей, кроме 1.

    Алгоритм:
    - Изначально считаем result = n.
    - Перебираем все простые делители p числа n.
    - Для каждого такого p вычитаем из result долю result//p.
    - Если после перебора делителей осталось n > 1, значит n — простой делитель, учитываем его.
    
    Например:
    φ(10) = 4, потому что числа 1,3,7,9 взаимно просты с 10.
    """
    result = n
    for p in range(2, int(math.sqrt(n)) + 1):
        if n % p == 0:
            while n % p == 0:
                n //= p  # делим n на p, пока делится
            result -= result // p  # вычитаем долю, соответствующую делителю p
    if n > 1:
        result -= result // n  # учитываем последний простой делитель, если он остался
    return result

# Пример:
print(f"φ(125) = {euler_phi(125)}")  # 125 = 5^3, φ(125) = 125 * (1 - 1/5) = 100

# Дополнительные примеры:
print(f"φ(10) = {euler_phi(10)}")    # 10 -> 1,3,7,9 = 4
print(f"φ(13) = {euler_phi(13)}")    # 13 — простое, φ(13) = 12
print(f"φ(36) = {euler_phi(36)}")    # 36 = 2^2 * 3^2, φ(36) = 36*(1-1/2)*(1-1/3) = 12

